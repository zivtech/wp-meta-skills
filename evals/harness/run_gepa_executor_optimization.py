#!/usr/bin/env python3
"""GEPA optimization of the WordPress plugin-executor persona against the deterministic gate.

This wraps ``gepa.optimize_anything``. The optimized CANDIDATE is the executor
persona (a system prompt). The EVALUATOR:

1. runs ``candidate_persona + task-spec fixture`` through codex/gpt-5.5 (hermetic,
   ``--sandbox read-only --ephemeral``, told not to use tools);
2. certifies the generated packet through the proven repair-loop certify path
   (``run_executor_repair_loop.make_certify``): static contract -> materialize ->
   (runtime profile) wp-env + WPCS + Plugin Check + WP activation;
3. returns a graded gate score plus the certifier's failure feedback as GEPA's
   Actionable Side Information (ASI) — the channel reflection uses to mutate the persona.

Reflection LM = the same codex/gpt-5.5 lane (codex is not a litellm provider, so it
is wrapped as a callable). The GENERATION model is held fixed to the baseline lane's
model, so "optimized persona vs seed persona" is a clean did-optimization-help test.

Why the runtime profile: the seed persona already passes the STATIC gate first-try on
the only hard fixture (no headroom). The single GEPA-addressable gap is the RUNTIME
gate's trivial distribution-metadata failure (missing GPL ``License:`` header, ``Tested
up to:`` line, placeholder URI) that keeps the persona at 10/11 zero-shot. The pass@1
target: evolve the persona to pass the runtime gate 11/11 zero-shot, baking in the one
repair the current persona needs. (See docs/wordpress/executor-gate-pass-experiment-2026-06-22.md.)

Reward-hacking guardrail: codex cannot touch the certifier or repo (read-only,
ephemeral, fresh temp cwd, no tools). At least one fixture is held out in ``--mode split``.

Honesty caveat: the gate certifies SHIPPABILITY (contract + materialization + WPCS +
Plugin Check + activation), NOT deep WordPress behavior. A gate win is not a behavior claim.

Modes:
  --dry-run         one seed generation + certify, print the score, no GEPA. Wiring check.
  --mode smoke      single-task search on one fixture (dataset=None). Plumbing test, NO holdout.
  --mode split      generalization: train on --train-fixture, hold out --holdout-fixture.

Examples:
  python3 evals/harness/run_gepa_executor_optimization.py --dry-run --profile runtime --run-dir /tmp/gepa-dry
  python3 evals/harness/run_gepa_executor_optimization.py --mode smoke --profile runtime --budget 24 --run-dir /tmp/gepa-smoke
"""

from __future__ import annotations

import argparse
import json
import sys
import tempfile
from pathlib import Path
from typing import Any

HARNESS = Path(__file__).resolve().parent
if str(HARNESS) not in sys.path:
    sys.path.insert(0, str(HARNESS))

import invoke  # noqa: E402  (repo-local harness module)
import run_executor_repair_loop as repair  # noqa: E402  (reuse proven static->runtime certify)

import gepa.optimize_anything as oa  # noqa: E402
from gepa.optimize_anything import (  # noqa: E402
    EngineConfig,
    GEPAConfig,
    ReflectionConfig,
    optimize_anything,
)

SUITE = "wordpress-plugin-executor"
EXECUTOR = "plugin"
FIXTURES_DIR = invoke.SUITES_ROOT / SUITE / "fixtures"
DEFAULT_FIXTURE = "abilities-ai-surface-v1"
RUNTIME_GATE_IDS = ("plugin_check", "wp_env_smoke")

OBJECTIVE = (
    "Optimize the WordPress plugin-executor PERSONA (a system prompt). The persona "
    "instructs a model to turn an approved planner spec into a single materializable "
    "WordPress plugin packet. Improve the persona so the packets it generates pass the "
    "deterministic certification gate zero-shot: packet contract, materialization, and "
    "(runtime) WPCS + Plugin Check + WordPress activation."
)
BACKGROUND = (
    "Each feedback block shows how a packet GENERATED BY the current persona failed the "
    "gate. The feedback is written as packet-repair guidance; translate it into "
    "persona-level instruction changes so future generations avoid the same failure "
    "without a repair round. Common gaps are distribution-metadata nits the gate enforces "
    "(GPL-compatible 'License:' and 'License URI:' plugin headers, a real 'Tested up to:' "
    "value, a non-placeholder 'Plugin URI'/text-domain). Keep the persona GENERAL across "
    "plugin specs - do not hard-code one fixture's filenames, slugs, or API list. Preserve "
    "the exact required output headings and the negative-space honesty rules (do not claim "
    "WPCS/PHPUnit/Plugin Check/wp-env/runtime behavior that was not actually run). Return "
    "the complete improved persona."
)


def load_fixtures() -> dict[str, str]:
    """Load every task-spec fixture (``*.md``, excluding ``*.metadata.yaml``)."""
    fixtures: dict[str, str] = {}
    for path in sorted(FIXTURES_DIR.glob("*.md")):
        if path.name.endswith(".metadata.yaml"):
            continue
        fixtures[path.stem] = path.read_text(encoding="utf-8")
    if not fixtures:
        raise FileNotFoundError(f"No fixtures found under {FIXTURES_DIR}")
    return fixtures


def seed_persona() -> str:
    """Resolve the executor persona used as GEPA's seed candidate."""
    path = invoke.agent_prompt_path(SUITE)
    if path is None:
        path = HARNESS.parent.parent / ".claude" / "skills" / SUITE / "SKILL.md"
    return path.read_text(encoding="utf-8").strip()


def score_from_verdict(verdict: dict[str, Any]) -> float:
    """Graded shippability score in [0, 1] from a repair-loop certify verdict.

    Monotonic by how far the artifact progressed, with the bulk of the gradient in the
    runtime band (where the 10/11 -> 11/11 headroom is):

    - full pass                 -> 1.0
    - runtime reached, k/n pass  -> 0.30 + 0.70 * (k/n)   (10/11 -> ~0.936)
    - static-only progress       -> 0.30 * (k/n)          (capped below the runtime band)
    """
    if verdict.get("passed"):
        return 1.0
    gv = verdict.get("gate_vector") or {}
    if not gv:
        return 0.0
    frac = sum(1 for v in gv.values() if v == "pass") / len(gv)
    runtime_reached = any(gate_id in gv for gate_id in RUNTIME_GATE_IDS)
    return round((0.30 + 0.70 * frac) if runtime_reached else (0.30 * frac), 4)


def generate_packet(
    persona: str, fixture_text: str, call_dir: Path, model: str | None, effort: str | None, timeout: int
) -> Path:
    """Run ``persona + fixture`` through the hermetic codex lane; save the packet."""
    prompt = persona.strip() + "\n\n---\n\n" + fixture_text.strip()
    rc, out, err, _dt = invoke._run_model(
        prompt, None, timeout, 2, provider="codex", model=model, effort=effort
    )
    packet = call_dir / "gen.packet.md"
    packet.write_text(out or "", encoding="utf-8")
    if not (out or "").strip():
        (call_dir / "gen.stderr.txt").write_text(err or f"exit={rc}", encoding="utf-8")
    return packet


def evaluate_once(
    persona: str, fixture_name: str, fixtures: dict[str, str], run_dir: Path, profile: str,
    model: str | None, effort: str | None, gen_timeout: int, cert_timeout: int,
) -> tuple[float, dict[str, Any]]:
    """One full generation + certification of a persona on a fixture -> (score, side_info)."""
    call_dir = Path(tempfile.mkdtemp(prefix=f"{fixture_name}-", dir=str(run_dir)))
    packet = generate_packet(persona, fixtures[fixture_name], call_dir, model, effort, gen_timeout)
    certify_fn = repair.make_certify(SUITE, EXECUTOR, call_dir, profile, cert_timeout)
    verdict = certify_fn(0, packet)
    score = score_from_verdict(verdict)
    side_info = {
        "Passed": bool(verdict.get("passed")),
        "Score": score,
        "Fixture": fixture_name,
        "Profile": profile,
        "GateVector": verdict.get("gate_vector") or {},
        "FailingGates": verdict.get("failing_gates") or [],
        "Feedback": verdict.get("failures") or "Gate passed; no failure feedback was emitted.",
        "WorkDir": str(call_dir),
    }
    return score, side_info


def make_evaluator(
    fixtures: dict[str, str], run_dir: Path, default_fixture: str, profile: str,
    model: str | None, effort: str | None, gen_timeout: int, cert_timeout: int,
):
    """Build the GEPA evaluator closure. ``example`` is a {'fixture': name} dict or None."""

    def evaluate(candidate: str, example: dict[str, Any] | None = None):
        fixture_name = example["fixture"] if example else default_fixture
        score, side_info = evaluate_once(
            candidate, fixture_name, fixtures, run_dir, profile, model, effort, gen_timeout, cert_timeout
        )
        oa.log(f"[{fixture_name}/{profile}] passed={side_info['Passed']} score={score} gates={side_info['GateVector']}")
        return score, side_info

    return evaluate


def make_reflection_lm(model: str | None, effort: str | None, timeout: int):
    """Wrap the codex lane as a GEPA reflection LM (accepts str or chat-messages list)."""

    def reflect(prompt) -> str:
        text = prompt if isinstance(prompt, str) else "\n\n".join(m.get("content", "") for m in prompt)
        _rc, out, _err, _dt = invoke._run_codex(text, timeout, 2, model=model, effort=effort)
        return out or ""

    return reflect


def summarize(result, run_dir: Path, args, model: str | None, effort: str | None) -> dict[str, Any]:
    """Persist GEPA result + best persona; return a small headline dict."""
    best = result.best_candidate
    best_text = best if isinstance(best, str) else next(iter(best.values()))
    (run_dir / "best_persona.md").write_text(best_text, encoding="utf-8")

    try:
        (run_dir / "result.json").write_text(
            json.dumps(result.to_dict(), indent=2, default=str), encoding="utf-8"
        )
    except Exception as exc:  # pragma: no cover - defensive dump
        (run_dir / "result.json").write_text(json.dumps({"to_dict_error": str(exc)}), encoding="utf-8")

    headline: dict[str, Any] = {
        "mode": args.mode,
        "profile": args.profile,
        "generation_model": model,
        "generation_effort": effort,
        "budget_max_metric_calls": args.budget,
        "total_metric_calls": getattr(result, "total_metric_calls", None),
        "num_candidates": getattr(result, "num_candidates", None),
        "best_idx": getattr(result, "best_idx", None),
    }
    try:
        scores = list(result.val_aggregate_scores)
        headline["seed_val_score"] = scores[0] if scores else None
        best_idx = getattr(result, "best_idx", None)
        headline["best_val_score"] = (
            scores[best_idx] if best_idx is not None and best_idx < len(scores) else None
        )
        headline["val_scores_all"] = scores
    except Exception as exc:  # pragma: no cover - accessor shape is version-dependent
        headline["val_score_error"] = str(exc)
    if args.mode == "split":
        headline["train_fixture"] = args.train_fixture
        headline["holdout_fixture"] = args.holdout_fixture
    return headline


def run_dry(args, fixtures, persona, model, effort) -> int:
    """One seed generation + certify; print the score. No GEPA, no search budget."""
    run_dir = Path(args.run_dir).resolve()
    run_dir.mkdir(parents=True, exist_ok=True)
    fixture_name = args.fixture or (args.train_fixture if args.mode == "split" else DEFAULT_FIXTURE)
    print(f"[dry-run] seed persona on '{fixture_name}' via codex/{model} ({effort}), profile={args.profile}...")
    score, side_info = evaluate_once(
        persona, fixture_name, fixtures, run_dir, args.profile, model, effort, args.gen_timeout, args.cert_timeout
    )
    printable = {k: v for k, v in side_info.items() if k != "Feedback"}
    print(json.dumps({"score": score, **printable}, indent=2, default=str))
    print(f"[dry-run] work dir: {side_info['WorkDir']}")
    return 0


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("--mode", choices=("smoke", "split"), default="smoke")
    parser.add_argument("--profile", choices=("static", "runtime"), default="runtime",
                        help="Certify profile. runtime = wp-env + WPCS + Plugin Check + activation (the gate with headroom).")
    parser.add_argument("--dry-run", action="store_true", help="One seed gen+certify, print score, skip GEPA.")
    parser.add_argument("--run-dir", required=True, help="Working/output dir (GEPA state under <run-dir>/gepa_state).")
    parser.add_argument("--budget", type=int, default=24, help="GEPA max_metric_calls (evaluation budget; hard cap).")
    parser.add_argument("--fixture", default=None, help=f"smoke/dry-run fixture (default: {DEFAULT_FIXTURE}).")
    parser.add_argument("--train-fixture", default=DEFAULT_FIXTURE, help="split-mode training fixture.")
    parser.add_argument("--holdout-fixture", default="smoke-wordpress-v1", help="split-mode held-out fixture.")
    parser.add_argument("--model", default=None, help="Override generation/reflection model (default: baseline lane).")
    parser.add_argument("--effort", default=None, help="Override reasoning effort (default: baseline lane).")
    parser.add_argument("--gen-timeout", type=int, default=300, help="Per-generation codex timeout (sec).")
    parser.add_argument("--cert-timeout", type=int, default=900, help="Per-certification timeout (sec; runtime is slow).")
    parser.add_argument("--reflect-timeout", type=int, default=300, help="Per-reflection codex timeout (sec).")
    parser.add_argument("--seed", type=int, default=0, help="GEPA RNG seed.")
    parser.add_argument("--parallel", action="store_true", help="Experimental: parallel evaluation (default sequential).")
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    fixtures = load_fixtures()
    persona = seed_persona()

    settings = invoke.get_invocation_settings(SUITE)
    _provider, model, effort = invoke.resolve_invocation_runtime(settings, "baseline-gepa", args.model, args.effort)

    default_fixture = args.fixture or DEFAULT_FIXTURE
    needed = {default_fixture} if args.mode == "smoke" else {args.train_fixture, args.holdout_fixture}
    for name in needed:
        if name not in fixtures:
            raise SystemExit(f"Fixture '{name}' not found. Available: {sorted(fixtures)}")

    if args.dry_run:
        return run_dry(args, fixtures, persona, model, effort)

    run_dir = Path(args.run_dir).resolve()
    run_dir.mkdir(parents=True, exist_ok=True)
    evaluator = make_evaluator(
        fixtures, run_dir, default_fixture, args.profile, model, effort, args.gen_timeout, args.cert_timeout
    )
    reflection_lm = make_reflection_lm(model, effort, args.reflect_timeout)

    config = GEPAConfig(
        engine=EngineConfig(
            max_metric_calls=args.budget,
            run_dir=str(run_dir / "gepa_state"),
            seed=args.seed,
            parallel=args.parallel,
            max_workers=None if args.parallel else 1,
            raise_on_exception=False,
            track_best_outputs=True,
            display_progress_bar=False,
        ),
        reflection=ReflectionConfig(reflection_lm=reflection_lm),
    )

    print(f"[gepa] mode={args.mode} profile={args.profile} budget={args.budget} model=codex/{model} ({effort}) run_dir={run_dir}")
    if args.mode == "smoke":
        print(f"[gepa] single-task on fixture '{default_fixture}' (NO holdout — plumbing test)")
        result = optimize_anything(
            seed_candidate=persona, evaluator=evaluator,
            objective=OBJECTIVE, background=BACKGROUND, config=config,
        )
    else:
        print(f"[gepa] train='{args.train_fixture}'  holdout='{args.holdout_fixture}'")
        result = optimize_anything(
            seed_candidate=persona, evaluator=evaluator,
            dataset=[{"fixture": args.train_fixture}],
            valset=[{"fixture": args.holdout_fixture}],
            objective=OBJECTIVE, background=BACKGROUND, config=config,
        )

    headline = summarize(result, run_dir, args, model, effort)
    (run_dir / "summary.json").write_text(json.dumps(headline, indent=2, default=str), encoding="utf-8")
    print("\n=== GEPA summary ===")
    print(json.dumps(headline, indent=2, default=str))
    print(f"\nBest persona -> {run_dir / 'best_persona.md'}")
    print("Caveat: gate = shippability (contract + materialization + WPCS + Plugin Check + activation), NOT deep WP behavior.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
